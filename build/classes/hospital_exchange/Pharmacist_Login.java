    private void jButton_LOGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LOGINActionPerformed
        PreparedStatement ps;
        ResultSet rs;
        String uname = jTextField1.getText();
     String pass = String.valueOf(jPasswordField1.getPassword());
     
     String query = "SELECT * FROM pharmacist_details WHERE username=? AND password=?";
     
        try {
            ps = MyConnection1.getConnection().prepareStatement(query);
            
            ps.setString(1,uname);
            ps.setString(2,pass);
            rs = ps.executeQuery();
            
            if (rs.next()){
                
                Pharmacist pc = new Pharmacist();
                pc.setVisible(true);
                pc.pack();
            
                pc.setExtendedState(JFrame.MAXIMIZED_BOTH);
              
                this.dispose();
                
            }
            else {
                JOptionPane.showMessageDialog(null, "Incorrect username or password");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Pharmacist_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
     
     
     
    }//GEN-LAST:event_jButton_LOGINActionPerformed

